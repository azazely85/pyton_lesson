n = int(input())
if (0 <= n <= 1000):
    if (11 <= n % 100 <= 14):
        print(n, 'программистов')
    elif (n % 10 == 1):
        print(n, 'программист')
    elif (2 <= n % 10 <= 4):
        print(n, 'программиста')
    elif ((n % 10 == 0) or (5 <= n % 10 <= 9)):
        print(n, 'программистов')
else:
    print('Введите число от 0 до 1000')